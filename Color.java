class Color
{
	public static void main(String args[])
	{
	    string c1='R';
	    if(c1='R')
		{
		    System.out.println("Red");
		}
	    else if(c1='B')
	    {
		System.out.println("Blue");
	    }
	    else if(c1='G')
	    {
		System.out.println("Green");
	    }
	    else if(c1='O')
	    {
		System.out.println("Orange");
	    }
	    else
	    {
		System.out.println("Invalid");
            }
	}
}